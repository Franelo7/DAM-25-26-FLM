package ud4Estudiar.clases;

public class Persona {
    String dni;
    String nombre;
    String telefono;
    String direccion;

    public Persona(String nombre, String dni, String telefono, String direccion) {
        this.nombre = nombre;
        this.dni = dni;
        this.telefono = telefono;
        this.direccion = direccion;
    }
}
